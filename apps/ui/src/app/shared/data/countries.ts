/**
 * ISO-3166-1 alpha-2 ülke listesi (kod + TR/EN ad).
 * Bayrak emojisi koddan türetilir (regional indicator sembolleri).
 * DE için isim EN'e düşer (ayrı liste tutmamak için).
 */
export interface Country {
  code: string; // ISO alpha-2 (büyük harf)
  tr: string;
  en: string;
}

/** ISO-2 kodundan bayrak emojisi üretir. Örn. 'TR' → 🇹🇷 */
export function flagOf(code: string): string {
  const cc = (code || '').trim().toUpperCase();
  if (cc.length !== 2 || !/^[A-Z]{2}$/.test(cc)) { return '🏳️'; }
  const A = 0x1f1e6;
  return String.fromCodePoint(A + cc.charCodeAt(0) - 65, A + cc.charCodeAt(1) - 65);
}

